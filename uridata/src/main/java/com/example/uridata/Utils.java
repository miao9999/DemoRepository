    /**
     * 从 uri 中获取图片的 path 值
     * @param context
     * @param uri
     * uri 的格式
     *
       [scheme:]scheme-specific-part[#fragment]
       [scheme:][//authority][path][?query][#fragment]
       [scheme:][//host:port][path][?query][#fragment]

     * @return
     */
    public static String getRealFilePath(final Context context, final Uri uri ) {
        if ( null == uri ) return null;
        final String scheme = uri.getScheme();
        String data = null;
        if ( scheme == null )
            data = uri.getPath();
        else if ( ContentResolver.SCHEME_FILE.equals( scheme ) ) {
            data = uri.getPath();
        } else if ( ContentResolver.SCHEME_CONTENT.equals( scheme ) ) {
            Cursor cursor = context.getContentResolver().query( uri, new String[] { MediaStore.Images.ImageColumns.DATA }, null, null, null );
            if ( null != cursor ) {
                if ( cursor.moveToFirst() ) {
                    int index = cursor.getColumnIndex( MediaStore.Images.ImageColumns.DATA );
                    if ( index > -1 ) {
                        data = cursor.getString( index );
                    }
                }
                cursor.close();
            }
        }
        return data;


    }



    /**
     * 裁剪图片
     *
     * @param activity    当前activity
     * @param orgUri      剪裁原图的Uri
     * @param desUri      剪裁后的图片的Uri
     * @param aspectX     X方向的比例
     * @param aspectY     Y方向的比例
     * @param width       剪裁图片的宽度
     * @param height      剪裁图片高度
     * @param requestCode 剪裁图片的请求码
     */
    public static void cropImageUri(Activity activity, Uri orgUri, Uri desUri, int aspectX, int aspectY, int width, int height, int requestCode) {
        try {
            Intent intent = new Intent("com.android.camera.action.CROP");
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.N) {
                intent.addFlags(Intent.FLAG_GRANT_READ_URI_PERMISSION);
                intent.addFlags(Intent.FLAG_GRANT_WRITE_URI_PERMISSION);
            }
            intent.setDataAndType(orgUri, "image/*");
            intent.putExtra("crop", "true");
            // 华为手机 当 aspectX 和 aspectY 的值一样时，自动变成裁剪圆形片，所以针对华为机型做适配
            boolean a = Build.MODEL.contains("HUAWEI");
            boolean b = Build.MANUFACTURER.equals("HUAWEI");

            if (a || b) {
                intent.putExtra("aspectX", aspectX + 1);
                intent.putExtra("aspectY", aspectY);
            } else {
                intent.putExtra("aspectX", aspectX);
                intent.putExtra("aspectY", aspectY);
            }

            intent.putExtra("outputX", width);
            intent.putExtra("outputY", height);
            intent.putExtra("scale", true);
            //将剪切的图片保存到目标Uri中
            intent.putExtra(MediaStore.EXTRA_OUTPUT, desUri);
            intent.putExtra("return-data", false);
            intent.putExtra("outputFormat", Bitmap.CompressFormat.JPEG.toString());
            intent.putExtra("noFaceDetection", true);
            activity.startActivityForResult(intent, requestCode);
        } catch (Exception e) {
            e.printStackTrace();
            Toast.makeText(activity, "无法获取该相册路径,请选择其他相册", Toast.LENGTH_SHORT).show();
        }
    }



